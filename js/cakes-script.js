const heart1 = document.querySelector('.like1');
heart1.onclick = function() {
    if (heart1.classList.toggle('like-change'))
        alert('Торт добавлен в "Понравившееся"')
    else{
        alert('Торт удален из "Понравившегося"')
    }
}

